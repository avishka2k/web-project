        <p> Human health define as the complete state of physical, social and mental well-being.
             According to the world health organization human health is a state of complete physical, 
             mental and social well-being and nor merely the absence of disease and infirmity. 
            What defines "excellent" health, in particular, might be difficult to define. Heavy shoveling after 
            a snowstorm may cause a heart attack in the rather frail individual who remains "well" inside the 
            typical surroundings of his or her existence. Alternatively, a sea-level resident may relocate to 
            the highlands and experience shortness of breath and anemia.
        </P>

    <h2>Importance of good healthy lifestyle.</h2>

        <p>The value of health to a person is that it saves him money on medical bills and the aggravation 
            of having to visit hospitals. A healthy individual perceives him saving money that he would have 
            spent on the disease if he hadn't taken care of his health. "An ounce of prevention is worth a 
            pound of cure," as the saying goes.
        </p>
        <p>Psychologically healthy people are at ease and content in their surroundings, and they enjoy 
            life to the fullest. A person suffering from mental illness is always perceived as pessimistic
            and gloomy. A healthy individual, on the other hand, appears to be happy in his life, forming 
            relationships with others, and displaying love, kindness, and closeness.
        </p>
        <img src="./images/Blog/bg_2.jpg" alt="">
    </div>

    <div class="blog-cantent01">
    <h2>Factors destroy healthy life style</h2>

        <p>You workout and eat nutritious foods in moderation, yet you still don't feel well. 
            It's likely that one (or all) of these five behaviors is destroying your otherwise healthy lifestyle. 
            Check out the top habits that will sabotage your healthy lifestyle, as well as our tips for breaking them.
        </p>
            <b>  1) Going to Bed Too Late </b>
        <p>Getting 8-10 hours of sleep each night will help you perform better in sports, boost your attention, 
            and recuperate from prior workouts. A disturbed night's sleep can cause increased hunger and decreased 
            fullness by altering the levels of Leptin and Ghrelin. It's crucial to remember that it's more necessary 
            to sleep for a long time than it is to go to bed at a certain time.
        </p>
            <span>HOW TO FIX IT:</span>
        <p>Aim for 8 hours every night and avoid caffeine after 7 pm if going to bed at 11 pm (or an equivalent time 
            depending on when you go to bed).
        </p>

            <b> 2) Eating Other People’s Food </b>
        <p>Some may consider it a no-no, but if you don't complete your children's or partner's meals, you may be 
            squandering them in the long term. Finishing your meal can cause you to consume more calories without 
            realizing it, so keep track of how much you're eating!
        </p>

            <span>HOW TO FIX IT:</span> 

        <p>Prepare smaller meals. Throw away your partner's or child's food. Save their leftovers for the next day's lunch.
           Make it a rule that you will only eat your meal and not anyone else's.
        </p>
            <b>  3) The Drunk Eating Cycle </b>
        <p>Excessive alcohol drinking can contribute to weight gain, partly due to the high calorie content of alcoholic 
            beverages and intoxicated eating. If at all possible, choose lower-calorie drinks, skip the bar snacks, order 
            the kebab (to avoid a calorie binge), and choose doner instead of doner. Preparing food for a hangover might 
            also help you avoid putting on weight.
        </p>

            <span>HOW TO FIX IT:</span>

        <p>If possible, look for lower-calorie drinks, drop the bar snacks, get the kebab (you’re only human), but pick 
            the shish kebab over the doner (saving you about 500 calories). Of course, having prepared food for the 
            hangover can also save you from a calorie-fest.
        </p>
            <b>  4)We aren’t connected with nature anymore.  </b>
        <p>There is a growing body of evidence on the benefits of getting outside, particularly the benefits of moderate, 
            non-burning sunshine exposure. You get ultraviolet light exposure even on cloudy days or in the shade. But 
            you have to be outside – sunlight through the glass isn’t the same – it does not produce vitamin D.
        </p>

            <span>HOW TO FIX IT:</span>

        <p>Try to get outside for 10-15 minutes every day between 10AM and 3PM. Do not wear sunscreen for the first little 
            bit of time you are outside (sunscreen blocks the synthesis of vitamin D) but DO NOT BURN. Put sunscreen on well 
            before you are in danger of getting a sunburn. Sunburns unequivocally increase your risk of deadly melanoma, so 
            DO NOT BURN.
        </p>
            <b>  5) We are addicts.<br>   </b>
        <p>Like lack of exercise, this one is plainly obvious. The health conscious plant-based folks reading this page may 
            think, “Oh that’s for other people.” But it’s not. I have long ago lost my sense of surprise when I meet someone 
            doing all the “right” nutrition things who tells me that they are a recovering addict still working to maintain 
            their sobriety. Wealthy, educated, poor, White, Black, suburban, rural, or urban – there’s no place addiction 
            doesn’t touch. The numbers are staggering. Tobacco, alcohol, and other drugs of abuse are ravaging families and 
            communities. These drugs are responsible for tremendous suffering, despair, and tragedy.
        </p>

            <span>HOW TO FIX IT:</span>

        <p>One of the most important, yet hardest, steps I think I’ve seen addicts struggle with: Surrender control and ask 
            for help. One of the most wonderful aspects of a healthy diet and lifestyle is that these factors are interconnected. 
            More exercise might yield better sleep, more sunshine, and more nature. Increased well-being from all of these help
            with self-care in a variety of other domains. I don’t suggest people work on changing too many behaviors at once, 
            but sometimes doing something good for one area helps with another area.
        </p>

    <h2>Impact of food on health</h2>

        <span>Fast Food Nutrition: Junk Food's Effect on Your Body</span>
        <p>Fast food nutrition should make up a minimal part of a healthy diet. Fast foods and junk foods are high in fat, sodium, 
            and sugar, which can lead to obesity and a range of attendant health problems, including diabetes, heart disease and 
            arthritis. Here are the facts about how excessive junk food consumption affects your body.
        </p>

    <img src="./images/Blog/bg_3.jpg" alt="">h
            <span> Junk Food Affects Your Energy Levels</span>
            <p>Junk food doesn't contain the nutrients your body needs to stay healthy. As a result, you may feel chronically 
                fatigued and lack the energy you need to complete daily tasks. The high levels of sugar in junk food puts your 
                metabolism under stress; when you eat refined sugar, your pancreas secretes high amounts of insulin to prevent 
                a dangerous spike in blood sugar levels. Because fast food and junk food don't contain adequate amounts of 
                protein and good carbohydrates, your blood sugar levels will drop suddenly after eating, leaving you feeling 
                grumpy, fatigued, and craving sugar.
            </p>
            <span>Junk Food Contributes to Poor Performance and Obesity</span>
            <p>Junk food contains large amounts of fat, and as fat accumulates in your body, you'll gain weight and could become 
                obese. The more weight you gain, the more you'll be at risk for serious chronic illnesses such as diabetes, heart 
                disease and arthritis. You could even have a heart attack.
            </p>
            <p>The high levels of fat and sodium in junk food can cause high blood pressure or hypertension. Excessive dietary 
                sodium can also have a negative effect on renal function, even leading to kidney disease.
            </p>
            <p>In the short term, high levels of dietary fat led to poor cognitive performance. You'll feel tired and have 
                trouble concentrating because your body might not be getting enough oxygen.
            </p>

            <span>Junk Food Can Damage Your Liver and Your Heart</span>
            <p>The high levels of fat and sodium in junk food and fast food can contribute to heart disease by raising blood 
                cholesterol levels and contributing to arterial plaque buildup. The high levels of trans fatty acids found 
                in many junk foods and fast foods can lead to fatty liver deposits, which, over time, can cause liver 
                dysfunction and disease.
            </p>

            
            <span>Junk Food Can Lead to Diabetes</span>
            <p>Over time, the high levels of sugar and simple carbohydrates in junk food can lead to type 2 diabetes. 
                This occurs because eating too much sugar puts your metabolism under stress; when you eat a lot of refined 
                white sugar and simple carbohydrates, your body must pump up insulin production to prevent a dangerous 
                spike in blood sugar levels.
            </p>
            <p>Because junk food doesn't contain the protein or complex carbohydrates that your body needs to maintain consistent
                blood sugar levels, your blood sugar levels will drop suddenly soon after eating. You'll crave sugar and likely 
                end up eating more junk food.
            </p>
            <p>Over time, this stress damages your body's ability to use the insulin secreted by your pancreases. A healthy 
                diet can help maintain your body's insulin sensitivity.
            </p>
            <p>Even in the short term, eating too much junk food can make you feel uncomfortable. It can lead to mood swings 
                and constipation and lower your energy levels so that you lack interest in the exercise you need to burn off 
                those extra calories.
            </p>

    <h2>Is exercise essential for good health</h2>
        
        <p>Want to feel better, have more energy and even add years to your life? Just exercise.</p>
        <p>The health benefits of regular exercise and physical activity are hard to ignore. Everyone benefits from exercise, 
            regardless of age, sex, or physical ability.
        </p>
        <p>Need more convincing to get moving? Check out these seven ways that exercise can lead to a happier, healthier you.</p>

    <img src="./images/Blog/bg_4.jpg" alt="">

            <span> Exercise controls weight</span>
            <p>Exercise can help prevent excess weight gain or help maintain weight loss. When you engage in physical activity, 
                you burn calories. The more intense the activity, the more calories you burn.
            </p>
            <p>Regular trips to the gym are great, but don't worry if you can't find a large chunk of time to exercise every day. 
                Any amount of activity is better than none at all. To reap the benefits of exercise, just get more active 
                throughout your day — take the stairs instead of the elevator or rev up your household chores. Consistency is key
            </p>

            <span>Exercise combats health conditions and diseases</span>
            <p>Worried about heart disease? Hoping to prevent high blood pressure? No matter what your current weight is, being 
                active boosts high-density lipoprotein (HDL) cholesterol, the "good" cholesterol, and it decreases unhealthy 
                triglycerides. This one-two punch keeps your blood flowing smoothly, which decreases your risk of cardiovascular 
                diseases.
            </p>
            <p>Regular exercise helps prevent or manage many health problems and concerns, including:</p>

                <ul>
                    <li>Stroke</li>
                    <li>Metabolic syndrome</li>
                    <li>High blood pressure</li>
                    <li>Type 2 diabetes</li>
                    <li>Depression</li>
                    <li>Anxiety</li>
                    <li>Many types of cancer</li>
                    <li>Arthritis</li>
                    <li>Falls</li>
                </ul>

            <p>It can also help improve cognitive function and helps lower the risk of death from all causes.</p>

            <span>Exercise improves mood</span>
            <p>Need an emotional lift? Or need to destress after a stressful day? A gym session or brisk walk can help. Physical 
                activity stimulates various brain chemicals that may leave you feeling happier, more relaxed, and less anxious.
            </P>
            <p>You may also feel better about your appearance and yourself when you exercise regularly, which can boost your 
                confidence and improve your self-esteem.
            </p>

            <span>Exercise boosts energy</span>
            <p>Winded by grocery shopping or household chores? Regular physical activity can improve your muscle strength and boost 
                your endurance.
          0  </p>
            <p>Exercise delivers oxygen and nutrients to your tissues and helps your cardiovascular system work more efficiently. 
                And when your heart and lung health improve, you have more energy to tackle daily chores.
            </p>

            <span>Exercise promotes better sleep</span>
            <p>Struggling to snooze? Regular physical activity can help you fall asleep faster, get better sleep, and deepen your 
                sleep. Just don't exercise too close to bedtime, or you may be too energized to go to sleep.
            </p>

            <span>Exercise puts the spark back into your sex life</span>
            <p>Do you feel too tired or too out of shape to enjoy physical intimacy? Regular physical activity can improve energy 
                levels and increase your confidence about your physical appearance, which may boost your sex life.
            </P>
            <p>But there's even more to it than that. Regular physical activity may enhance arousal for women. And men who exercise 
                regularly are less likely to have problems with erectile dysfunction than are men who don't exercise.
            </p>

            <span>Exercise can be fun … and social!</span>
            <p>Exercise and physical activity can be enjoyable. They give you a chance to unwind, enjoy the outdoors or simply engage 
                in activities that make you happy. Physical activity can also help you connect with family or friends in a fun social 
                setting.
            </p>
            <p>So take a dance class, hit the hiking trails or join a soccer team. Find a physical activity you enjoy, and just do it. 
                Bored? Try something new or do something with friends or family.
            </p>

            <span>The bottom line on exercise</span>
            <p>Exercise and physical activity are great ways to feel better, boost your health and have fun. For most healthy adults, 
                the U.S. Department of Health and Human Services recommends these exercise guidelines:
            </p>
                <ul>
                    <li> <span>Aerobic activity</span> Get at least 150 minutes of moderate aerobic activity or 75 minutes of vigorous 
                    aerobic activity a week, or a combination of moderate and vigorous activity. The guidelines suggest that you spread 
                    out this exercise during a week. To provide even greater health benefit and to assist with weight loss or maintaining 
                    weight loss, at least 300 minutes a week is recommended. But even small amounts of physical activity are helpful. 
                    Being active for short periods of time throughout the day can add up to provide health benefit.
                    </li>
                    <li><span>Strength training.</span> Do strength training exercises for all major muscle groups at least two times a week. 
                    Aim to do a single set of each exercise using a weight or resistance level heavy enough to tire your muscles after about 
                    12 to 15 repetitions.
                    </li>
                </ul>
            
            <p>Moderate aerobic exercise includes activities such as brisk walking, biking, swimming and mowing the lawn. Vigorous aerobic 
                exercise includes activities such as running, heavy yardwork and aerobic dancing. Strength training can include use of weight 
                machines, your own body weight, heavy bags, resistance tubing or resistance paddles in the water, or activities such as 
                rock climbing.
            </P>
            <p>If you want to lose weight, meet specific fitness goals, or get even more benefits, you may need to ramp up your moderate 
                aerobic activity even more.
            </P>
            <p>Remember to check with your doctor before starting a new exercise program, especially if you have any concerns about your 
                fitness, haven't exercised for a long time, have chronic health problems, such as heart disease, diabetes, or arthritis
            </p>


