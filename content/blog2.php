    <div class="blog-cantent01">
    <h2>Vegetarian meat substitutes</h2>

        <p>There is a large range of vegetarian meat substitute products on the market right now, 
            and some of us may see them as a healthier option. Plant based burgers, sausages, 
            mince, chicken alternatives, steaks, fish alternatives, kievs, pastries, pies and 
            more are available to buy. We did some research and found these products are in 
            fact processed and have long ingredient lists. Protein content varied, with 1 in 4 
            products lacking adequate protein. Meat substitutes are low in energy, sugar, and 
            saturated fat, and some are a source of fiber (unlike meat). You can still enjoy 
            meat alternative products but remember they are processed foods so they should not 
            be used as sole replacements for protein rich foods, and intake should be limited.
            Our advice is to read food labels and look for products that are a source of protein and 
            lower in saturated fat, sugar, and salt. There are lots of other dietary sources of 
            protein that you can choose from including meat, poultry, eggs, beans, lentils, nuts, 
            tofu, and chickpeas.
        </p>
