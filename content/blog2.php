    <h2>What should we be eating to get an adequate amount of protein?</h2>

        <p>Protein belongs to the meat, poultry, fish, eggs, beans and nuts group. 
            Two servings per day from this group are recommended. Beans, peas and lentils are 
            good sources of plant protein and are low in fat. Meat is a rich source of protein, 
            so if you decide to exclude or reduce your meat intake you will need to get your 
            protein from other foods. Eating a variety of different food sources that are 
            high in protein, from lentils to eggs will ensure that you get what you need.
        </p>
        <p>Plant sources of protein can be very convenient and versatile. They have shorter cooking times, 
            they can be cheaper to buy (especially canned peas, beans, lentils) but still delicious! 
            Personally, I have made an effort to try new meat free meals this year. For example, 
            the curried chickpea and sweet potato burger is a go-to of mine because I can have it 
            for lunch (in a wrap with hummus, or a salad) or for dinner in a bun with some homemade chips. 
            If that doesn’t tickle your fancy, have a look at our meat-free recipes.
        </p>
        <img src="../images/Blog/bg_2.jpg" alt="">
